"""Нейтральные наблюдения по эстетическим чертам лица."""

from __future__ import annotations

from typing import Any


def _band(score: float) -> str:
    """Грубая градация метрики."""
    if score >= 88:
        return "high"
    if score >= 72:
        return "balanced"
    if score >= 55:
        return "moderate"
    return "notable"


